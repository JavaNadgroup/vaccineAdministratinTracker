package org.health.administration;

import jakarta.servlet.RequestDispatcher;
import java.io.IOException;
import java.io.PrintWriter;
import jakarta.servlet.ServletException;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;

/**
 *
 * @author ogrey
 */
public class PatientForm extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        
            String nin = request.getParameter("nin");
            String name = request.getParameter("name");
            String  hospital = request.getParameter("hospital");
            String date = request.getParameter("date");
            String batch_number = request.getParameter("batch_number");
            String vaccine = request.getParameter("vaccine");
            
            if(nin !="" && name !="" && hospital !="" && date !="" && batch_number !="" && vaccine !=""){
            request.setAttribute("nin", nin);
            request.setAttribute("name", name);
            request.setAttribute("hospital", hospital);
            request.setAttribute("date", date);
            request.setAttribute("batch_number", batch_number);
            request.setAttribute("date", date);
            request.setAttribute("vaccine", vaccine);
            
            RequestDispatcher rs=request.getRequestDispatcher("PatientReg.jsp");
            rs.forward(request, response);
            }else{
                response.sendRedirect("http://localhost:8080/vaccinationTracker/PatientRegForm.jsp");
            }
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            out.println("<!DOCTYPE html>");
            out.println("<html>");
            out.println("<head>");
            out.println("<title>Servlet PatientForm</title>");            
            out.println("</head>");
            out.println("<body>");
            out.println("<h1>Servlet PatientForm at " + request.getContextPath() + "</h1>");
            out.println("</body>");
            out.println("</html>");
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
